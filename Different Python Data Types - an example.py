# an example for different Python data types such as
# Number(Integer, Float, Complex),
# String, Boolean, List, Tuple, Set and Dictionary.

print(type(7)) # int
print(type(11.10)) # float
print(type(4-11j)) # complex
print(type('Python is fun')) # str
print(type(True)) # bool
print(type(['The','Above','Is','True',7,11.10,4-11j,True])) # list
print(type((1,[2,3],False,'456789'))) # tuple
print(type({1,(2,3),True})) # set
print(type({1:2,3:4,True:False})) # dict

